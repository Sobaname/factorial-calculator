# Импортируем библиотеку math для использования math.factorial()
# Это обеспечивает оптимизацию для работы с большими числами,
# так как функция написана на C и работает очень эффективно.
import math

def calculate_factorial(n):
    """
    Вычисляет факториал числа n с помощью оптимизированной функции из библиотеки math.
    
    Аргументы:
        n (int): Неотрицательное целое число.
    
    Возвращает:
        int: Факториал числа n.
    """
    return math.factorial(n)

def main():
    """
    Основная функция программы, которая управляет взаимодействием с пользователем.
    """
    print("Программа для вычисления факториала числа.")
    
    try:
        # Запрашиваем ввод от пользователя и пытаемся преобразовать в целое число
        user_input = input("Пожалуйста, введите положительное целое число: ")
        number = int(user_input)
        
        # Проверяем, является ли число положительным
        if number < 0:
            print("Ошибка: Факториал определен только для неотрицательных целых чисел.")
            print("Пожалуйста, запустите программу снова и введите положительное число.")
        else:
            # Вычисляем факториал и выводим результат
            result = calculate_factorial(number)
            print(f"Факториал числа {number} равен {result}")
            
            # Демонстрация работы с большими числами
            if number > 10:
                print(f"Это большое число! Факториал {number} содержит {len(str(result))} цифр.")
    
    except ValueError:
        # Обрабатываем случай, когда ввод не может быть преобразован в целое число
        print("Ошибка: Введенные данные не являются целым числом.")
        print("Пожалуйста, запустите программу снова и введите корректное положительное целое число.")

# Точка входа в программу
if __name__ == "__main__":
    main()
